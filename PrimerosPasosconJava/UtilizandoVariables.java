public class UtilizandoVariables {
    public static void main(String[] args) {
        // Declaración de variables y asignación de valores
        boolean esEstudiante = true;
        char inicial = 'C';
        String saludo = "Hola, mundo";
        int numeroEntero = 42;
        long numeroLargo = 123456789L;
        float numeroDecimal = 3.14f;
        
        // Imprimir en consola el contenido de todas las variables
        System.out.println("Es estudiante: " + esEstudiante);
        System.out.println("Inicial: " + inicial);
        System.out.println("Saludo: " + saludo);
        System.out.println("Número Entero: " + numeroEntero);
        System.out.println("Número Largo: " + numeroLargo);
        System.out.println("Número Decimal: " + numeroDecimal);
        
        // Declaración de variables para nombre y edad
        String nombre = "Cayo Jean";
        int edad = 25; // Reemplaza con tu edad
        
        // Imprimir en pantalla el texto con nombre y edad
        System.out.println("Mi nombre es " + nombre + ", y tengo " + edad + " años.");
    }
}


